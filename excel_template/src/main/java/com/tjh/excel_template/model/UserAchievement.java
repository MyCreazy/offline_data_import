public class UserAchievement {
    private  String user_name;
    private  String year_target;
    private  String month_target;

    public String getUser_name() {
        return user_name;
    }

    public void setUser_name(String user_name) {
        this.user_name = user_name;
    }

    public String getYear_target() {
        return year_target;
    }

    public void setYear_target(String year_target) {
        this.year_target = year_target;
    }

    public String getMonth_target() {
        return month_target;
    }

    public void setMonth_target(String month_target) {
        this.month_target = month_target;
    }
}
